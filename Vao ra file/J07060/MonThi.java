package J07060;

public class MonThi {
    private String ma, ten, hinhThucthi;

    public MonThi(String ma, String ten, String hinhThucthi) {
        this.ma = ma;
        this.ten = ten;
        this.hinhThucthi = hinhThucthi;
    }

    public String getMa() {
        return ma;
    }

    public String getTen() {
        return ten;
    }
}
