package Quan_he_giua_cac_lop.J06008;

public class MonHoc {
    private String maMon, tenMon;

    public MonHoc() {
    }

    public MonHoc(String maMon, String tenMon) {
        this.maMon = maMon;
        this.tenMon = tenMon;
    }

    public String getMaMon() {
        return maMon;
    }

    public String getTenMon() {
        return tenMon;
    }
}
