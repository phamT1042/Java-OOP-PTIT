package J07036;

public class MonHoc {
    private String maMon, tenMon;
    private int soTC;

    public MonHoc(String maMon, String tenMon, int soTC) {
        this.maMon = maMon;
        this.tenMon = tenMon;
        this.soTC = soTC;
    }

    public String getMaMon() {
        return maMon;
    }

    public String getTenMon() {
        return tenMon;
    }
}
